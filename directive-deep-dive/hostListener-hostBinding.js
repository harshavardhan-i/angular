// • Hostlistener
// ○ It is a kind of event listener 
// ○ It can be used to listen to the events occuring on the element our directive is sitting on
// ○ Can be used just like the @Input() directive
// ○ Expects reserved arguments, like 'mouseenter', 'mouseleave' etc
// ○ However needs a function to be defines infront of the decorator instead of var
// ○ This function in itself takes event as parameters  
// • HostBinding
// ○ Is used for binding to a specific property of the hosted element
// ○ Could be Style etc.
